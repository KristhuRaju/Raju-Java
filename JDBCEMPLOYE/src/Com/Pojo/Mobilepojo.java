package Com.Pojo;

public class Mobilepojo {
	private int mid;
	private String msim;
	private int mnuber;
	private int fkpid;
	
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	public String getMsim() {
		return msim;
	}
	public void setMsim(String msim) {
		this.msim = msim;
	}
	public int getMnuber() {
		return mnuber;
	}
	public void setMnuber(int mnuber) {
		this.mnuber = mnuber;
	}
	public int getFkpid() {
		return fkpid;
	}
	public void setFkpid(int fkpid) {
		this.fkpid = fkpid;
	}
	

}
